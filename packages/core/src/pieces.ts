export function getCells(piece: PieceState): Cell[] {
  return SHAPES[piece.type][piece.rotation].map(([x, y]) => [piece.x + x, piece.y + y]);
}

export function getKickTests(type: PieceType, from: number, to: number): Cell[] {
  if (type === "O") return [[0, 0]];
  const key = `${from}>${to}`;
  const table = type === "I" ? I_KICKS : JLSTZ_KICKS;
  return table[key] ?? [[0, 0]];
}
